        public override string[] GetRolesForUser(string username)
        {
            using (var context = new Employee_Advertisement_PortalEntity())
            {
                var result = (from role in context.UserRole_tbl
                              join emp in context.EmployeeDetails_tbl on role.RoleId equals emp.RoleId
                              where emp.Email == username
                              select role.Role).ToArray();
                return result;
            }
